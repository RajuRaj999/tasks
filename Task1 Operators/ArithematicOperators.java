import java.io.*;
public class ArithematicOperators {
	public static void main(String[] args) {
		int a=295;
		double b=396.00d;
		System.out.println("Addition:"+(a+b));     //0
		System.out.println("Subtraction:"+(a-b));	//-101.0
		System.out.println("Multiplication:"+(a*b));	//116820.0
		System.out.println("Division:"+(a/b));		//0.74494949494
		System.out.println("Modulus:"+(a%b));	//295.0
	}

}